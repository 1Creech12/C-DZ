    if (a_4 < 1 || a_4 > 20) {
        cout << "Ошибка: число должно быть в диапазоне от 1 до 20!\n";
        return 1;
    }
    long long sum_4 = 1;
    int b_4 = a_4;
    while (b_4 <= 20)
    {
        sum_4 = sum_4 * b_4;
        b_4 = b_4 + 1;
    }
    cout << "Произведение чисел от " << a_4 << " до 20 = " << sum_4 << "\n\n";

