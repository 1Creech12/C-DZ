    if (y < 0) {
        cout << "Ошибка";
    }
    int a_2 = 1, b_2 = y;
    while (b_2 > 0) {
        a_2 = a_2 * x;
        b_2 --;
    }
    cout << x << " в степени " << y << " = " << a_2 << "\n\n";

